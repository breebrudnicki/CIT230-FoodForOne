    <h3>Scenario List</h3>
    <ul>
        <li>What is something else I can eat that is easy?</li>
        <li>What can I make to entertain some friends?</li>
        <li>What should I get at the grocery store?</li>
        <li>What are pantry staples?</li>
        <li>How can I eat a little healthier?</li>
        <li>How fast can I find what I need on your site?</li>
        <li>How long will these take?</li>
        <li>How much will this cost?</li>
        <li>How can I eat on a low-budget?</li>
        <li>What are some quick snacks I can eat?</li>
    </ul>
    <h3>Content</h3>
    <ul>
        <li>Grocery list templates
            <ul>
                <li>A couple different lists of groceries and ideas. There will be a super basic list of groceries that have staples everyone should always have on hand. There will also be a list of things to keep on hand if you enjoy baking frequently. There can also be a grocery list of things to keep on hand if you plan on eating sandwiches everyday. At the top of the page there will be a photo of groceries. Ideally, I think it would be cool to have a grocery list maker, so the user could look at recipes and put together their own list based on the things they want to eat that week.</li>
            </ul>
        </li>
        <li>Tips and Tricks to eating healthy
            <ul>
                <li>In this section I want to include advice about how to include fruits and vegetables in a diet, how to eat more than just carbs, and a guide to how long vegetables stay good. I plan to have a photo of fruits/veggies/something that looks good with the content. This may be a good section to include a video about how to eat healthy.</li>
            </ul>
        </li>
        <li>How to get started
            <ul>
                <li>This will give an explanation of how to use the site and also how to get started cooking for one's self. This will also include a list of basic utensils etc. to have in the kitchen. It would explain things that are necessary and things that would be nice to have. This section will include a photo of a student in the kitchen.</li>
            </ul>
        </li>
        <li>Quick Snacks
            <ul>
                <li>This section will include recipes for quick snacks and a list of ideas for quick snacks that take little to no preparation (like a list of veggies that make good snacks, etc.). I also want to include lists of my favorite fun snack ideas from other websites (like those videos that get shared on Facebook all the time).</li>
            </ul>
        </li>
        <li>Food for Entertaining
            <ul>
                <li>This section will include fun ideas for entertaining friends. It'll include desserts, a few larger dishes, and just a couple of tips about having people over. A photo of friends hanging out together will be included.</li>
            </ul>
        </li>
        <li>Treats
            <ul>
                <li>This section will include dessert recipes for one person and a picture of desserts. There will be a photo at the top of page. In addition to that, each recipe will have at least one photo included.</li>
            </ul>
        </li>
        <li>How to eat on a budget
            <ul>
                <li>This section will include tips about buying food on sale and finding coupons. It will also talk about freezing food and making food ahead of time and saving it for later.</li>
            </ul>
        </li>
        <li>Breakfast Ideas
            <ul>
                <li>This section will include breakfast recipes for one person. Each recipe will include a photo. One recipe I know I want to include is overnight oats, it's a super quick breakfast idea that can be made ahead of time for one person.</li>
            </ul>
        </li>
        <li>Lunch Ideas
            <ul>
                <li>This section will include lunch recipes for one person. Each recipe will include a photo. I want to include some fun sandwich ideas too.</li>
            </ul>
        </li>
        <li>Dinner Ideas
            <ul>
                <li>This section will also have a photo at the top, probably of a recipe included. There will be recipes of dinner ideas for one person included in here. Each recipe will have a photo included as well. Within dinner ideas there will be another section with crock pot meals.</li>
            </ul>
        </li>
    </ul>
