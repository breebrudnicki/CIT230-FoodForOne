    <h1>Site Plan</h1>
    <h3>Site Name:</h3>
    <p>Food for One / <a href=".">foodforone.net</a></p>
    <h3>Purpose Statement</h3>
    <p>Cooking for one can be hard. Iâ€™ve experienced that as a college student. I want to make a website where single college students and twenty-something's can find easy recipes in just the right portion size. Along with recipes, I want to provide a basic grocery list, tips and tricks to eating healthier, and ways to stay on a budget without starving. </p>
    <h3>Target Audience</h3>
    <ul>
        <li>Who: Single students/ twenty-something's wanting to cook for themselves</li>
        <li>Age: 18-35</li>
        <li>Technology Use: heavy</li>
        <li>Income: low</li>
        <li>Education: any</li>
        <li>Motivation: Wants to learn more about cooking</li>
    </ul>
