            <h3>Ingredients</h3>
            <ul>
                <li>Pasta (I always pick bowties!)</li>
                <li>Butter</li>
                <li>Italian Seasoning</li>
            </ul>
            <h3>Directions</h3>
            <ol>
                <li>Fill a pot with water and bring to a boil.</li>
                <li>Once water is boiling add in pasta and cook until done.</li>
                <li>Once the pasta is done cooking poor it into a strainer to get rid of excess water.</li>
                <li>While pasta is still warm place in bowl and put butter on top. Let it melt and stir so butter evenly coats the noodles.</li>
                <li>Add Italian seasoning to taste.</li>
                <li>Enjoy!</li>
