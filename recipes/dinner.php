            <h1>Easy Wedge Potatoes</h1>
            <img src="../images/recipes/potatowedges.jpg" alt="Potato Wedges" class="tall">
            <p>This is among my favorite side dishes because one, potatoes, and two, it's so easy.</p>
            <h3>Ingredients</h3>
            <ul>
                <li>1 Potato (or more if you want more wedges)</li>
                <li>Canola oil</li>
                <li>Salt and pepper</li>
                <li>Other seasonings of your choice</li>
            </ul>
            <h3>Instructions</h3>
            <ol>
                <li>Preheat the oven to 450 degrees.</li>
                <li>Wash the potato.</li>
                <li>On a cutting board, cut the potato into fourths lengthwise. Then cut it into smaller chunks the other way. You'll end up with sort of a triangle shape.</li>
                <li>(Optional) Place potatoes in a bowl of ice water for about 30 minutes. (Honestly, I always skip this step because I'm lazy and they turn out good. However, completing this step can remove the starch and make your potatoes crispier). </li>
                <li>In a zip-lock bag mix potatoes, oil (use enough to coat the potatoes you're cooking, eyeball it), and any seasonings you are using (including salt and pepper), until potatoes are evenly coated.</li>
                <li>Place potatoes evenly on baking pan and place in oven for 30 minutes or until golden brown and crispy.</li>
                <li>Enjoy!</li>
            </ol>
